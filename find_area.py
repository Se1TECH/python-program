# Find area of circle,rectangle and square

radius = int(input("Enter Radius of circle: "))
rHeight = int(input("Enter Height of rectangle: ")) 
rWidth = int(input("Enter width of rectangle: ")) 
sWidth = int(input("Enter width of Square: ")) 

print("Area of Circle is: " , 3.14*radius*radius)
print("Area of Rectangle is: " , rHeight*rWidth)
print("Area of Square is: " , 4*sWidth)
